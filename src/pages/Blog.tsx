const Blog = () => {
  const [selectedPost, setSelectedPost] = useState<number | null>(null);

  const handlePostSelect = (postId: number) => {
    setSelectedPost(postId);
  };

  const handleBackToIndex = () => {
    setSelectedPost(null);
  };

  const formatContent = (content: string) => {
    const lines = content.trim().split('\n');
    const elements: JSX.Element[] = [];
    let key = 0;

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      
      if (line.startsWith('## ')) {
        elements.push(
          <h2 key={key++} className="text-2xl font-bold text-foreground mt-8 mb-4">
            {line.substring(3)}
          </h2>
        );
      } else if (line.startsWith('### ')) {
        elements.push(
          <h3 key={key++} className="text-xl font-semibold text-foreground mt-6 mb-3">
            {line.substring(4)}
          </h3>
        );
      } else if (line.startsWith('- **')) {
        elements.push(
          <li key={key++} className="ml-6 mb-2 text-muted-foreground">
            <span dangerouslySetInnerHTML={{
              __html: line.substring(2).replace(/\*\*(.*?)\*\*/g, '<strong class="text-foreground font-semibold">$1</strong>')
            }} />
          </li>
        );
      } else if (line.includes('**')) {
        elements.push(
          <p key={key++} className="mb-4 text-muted-foreground leading-relaxed">
            <span dangerouslySetInnerHTML={{
              __html: line.replace(/\*\*(.*?)\*\*/g, '<strong class="text-foreground font-semibold">$1</strong>')
            }} />
          </p>
        );
      } else if (line.trim() === '') {
        // Skip empty lines
      } else {
        elements.push(
          <p key={key++} className="mb-4 text-muted-foreground leading-relaxed">
            {line}
          </p>
        );
      }
    }

    return elements;
  };

  return (
    <div className="min-h-screen">
      <Navigation />
      
      <main className="pt-20">
        <div className="max-w-4xl mx-auto px-4 sm:px-6 lg:px-8 py-12">
          {selectedPost === null ? (
            // Blog Index
            <div>
              <div className="flex items-center justify-between mb-8">
                <div>
                  <h1 className="text-4xl font-bold text-foreground mb-4">Blog</h1>
                  <p className="text-muted-foreground">
                    Insights and tips for natural healing and wellness
                  </p>
                </div>
                <Link to="/">
                  <Button variant="outline" className="flex items-center gap-2">
                    <ArrowLeft className="w-4 h-4" />
                    Back to Home
                  </Button>
                </Link>
              </div>

              <div className="grid gap-6">
                {blogPosts.map((post) => (
                  <Card 
                    key={post.id} 
                    className="cursor-pointer hover:shadow-lg transition-shadow"
                    onClick={() => handlePostSelect(post.id)}
                  >
                    <CardHeader>
                      <CardTitle className="text-xl hover:text-healing transition-colors">
                        {post.title}
                      </CardTitle>
                      <CardDescription className="flex items-center gap-4 text-sm">
                        <span className="flex items-center gap-1">
                          <Calendar className="w-4 h-4" />
                          {new Date(post.date).toLocaleDateString()}
                        </span>
                        <span className="flex items-center gap-1">
                          <User className="w-4 h-4" />
                          {post.author}
                        </span>
                      </CardDescription>
                    </CardHeader>
                    <CardContent>
                      <p className="text-muted-foreground">{post.excerpt}</p>
                    </CardContent>
                  </Card>
                ))}
              </div>
            </div>
          ) : (
            // Blog Post Detail
            <div>
              <div className="mb-8">
                <Button 
                  variant="outline" 
                  onClick={handleBackToIndex}
                  className="flex items-center gap-2 mb-6"
                >
                  <ArrowLeft className="w-4 h-4" />
                  Back to Blog Index
                </Button>
                
                {(() => {
                  const post = blogPosts.find(p => p.id === selectedPost);
                  if (!post) return null;
                  
                  return (
                    <article className="prose prose-lg max-w-none">
                      <header className="mb-8">
                        <h1 className="text-4xl font-bold text-foreground mb-4">
                          {post.title}
                        </h1>
                        <div className="flex items-center gap-4 text-muted-foreground">
                          <span className="flex items-center gap-1">
                            <Calendar className="w-4 h-4" />
                            {new Date(post.date).toLocaleDateString()}
                          </span>
                          <span className="flex items-center gap-1">
                            <User className="w-4 h-4" />
                            {post.author}
                          </span>
                        </div>
                      </header>
                      
                      <div className="prose-content">
                        {formatContent(post.content)}
                      </div>
                    </article>
                  );
                })()}
              </div>
            </div>
          )}
        </div>
      </main>
      
      <Footer />
    </div>
  );
};

